package adjlab3;

public class StringCreationDemo {
    public static void main(String[] args) {
        String str1 = "Hello";
        String str2 = new String("World");
        System.out.println("String 1: " + str1);
        System.out.println("String 2: " + str2);
        System.out.println("Combined: " + str1 + " " + str2);
    }
}
